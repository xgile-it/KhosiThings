package staticblock;

public class IfStatement {


    public static void main(String[]args)
    {
        int exam =84;
        char grades;

        if(exam >= 90)
        {
            grades='A';
        }
        if(exam >= 80)
        {
            grades='B';
        }
        if (exam >= 70)
        {
            grades='C';



        }
        else
        {
            grades='D';
        }
    }
}
